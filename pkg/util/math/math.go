package math

func Gcd(x, y int) int {
	var tmp int
	for {
		tmp = x % y
		if tmp > 0 {
			x = y
			y = tmp
		} else {
			return y
		}
	}
}

func Lcm(x, y int) int {
	return x * y / Gcd(x, y)
}

